//Array Declaration

class ArrayDemo3{
    public static void main(String args[]){
        int arr1[]={10,20,30,40,50};

        char arr2[]={'A','B','C','D','E'};

        float arr3[]={10.5f,20.3f,40.60f};

        boolean arr4[]={true,false,true};

        //Printing arr1
        System.out.println(arr1[0]);        //10
        System.out.println(arr1[1]);        //20
        System.out.println(arr1[2]);        //30
        System.out.println(arr1[3]);        //40
        System.out.println(arr1[4]);        //50

        //printing arr2
        System.out.println(arr2[0]);        //A
        System.out.println(arr2[1]);        //B
        System.out.println(arr2[2]);        //C
        System.out.println(arr2[3]);        //D
        System.out.println(arr2[4]);        //E

        //Printing arr3
        System.out.println(arr3[0]);        //10.5
        System.out.println(arr3[1]);        //20.3
        System.out.println(arr3[2]);        //40.60

        //printing arr4
        System.out.println(arr4[0]);        //true
        System.out.println(arr4[1]);        //false
        System.out.println(arr4[2]);        //true
        
        
    }
}